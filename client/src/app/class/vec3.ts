export class Vec3 {
  x: number = 0;
  y: number = 0;
  z: number = 0;

  constructor(_x: number = 0, _y: number = 0, _z: number = 0) {
    this.x = _x;
    this.y = _y;
    this.z = _z;
  }
}

export class Vec2 {
  x: number = 0;
  y: number = 0;

  constructor(_x: number = 0, _y: number = 0) {
    this.x = _x;
    this.y = _y;
  }
}